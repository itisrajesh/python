# Assignment Operator
a = 10
b = 3

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)
print("Floor Division:", a // b)

# Comparison Operators
print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater Than:", a > b)
print("Less Than:", a < b)
print("Greater Than or Equal:", a >= b)
print("Less Than or Equal:", a <= b)

# Logical Operators
x = True
y = False
print("And:", x and y)
print("Or:", x or y)
print("Not:", not (x))

# Augmented Assignment Operators
a += b
print("Add and Assign:", a)
a -= b
print("Subtract and Assign:", a)
a *= b
print("Multiply and Assign:", a)
a /= b
print("Divide and Assign:", a)
a %= b
print("Modulus and Assign:", a)
a **= b
print("Exponentiation and Assign:", a)
a //= b
print("Floor Division and Assign:", a)

# Logical Operator
temp = [11, 12, 13, 14, 15]
t = 13
print("'in' Operator :", t in temp)
print("'not in' Operator :", t not in temp)

# Bitwise operator
a = 5
b = 3
print("a=", bin(a), " b=", bin(b))
print("Bitwise AND: ", a & b)
print("Bitwise OR: ", a | b)
print("Bitwise XOR: ", a ^ b)
print("Bitwise NOT: ", ~a)
print("Left Shift: ", a << 1)
print("Right Shift: ", a >> 1)

# Identity Operators
print("Is a same as b:", a is b)
print("Is a not same as b:", a is not b)

